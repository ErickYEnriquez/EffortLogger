package model;

public class log{
	private int logID;
	private String Description;
	public String getLogID() {
		return "" + logID;
	}
	public void setlogID(int newlogID) {
		logID = newlogID;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}	

}